namespace Status_Editer.User_Control.MainForm {
	[ToolboxItem(true)]
	public partial class MainFormStripMenu : UserControl {
		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
		// Initialize
		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

		// イベントの登録を許可
		[Browsable(true)]

		// Delegate
		/// <summary>
		/// MenuExitClickイベントのデリゲート宣言
		/// </summary>
		/// <param name="sender">object</param>
		/// <param name="e">EventArgs</param>
		public delegate void EventMenuExitClickHandler(object sender, EventArgs e);

		// EventHandler
		// イベントの宣言
		[Category("Action")]
		[Description("「ファイル」→「アプリケーションの終了」をクリックした時に発動するイベントです")]
		public event EventMenuExitClickHandler MenuExitClick;


		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
		// Property
		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
		// Constructor Method
		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

		/// <summary>
		/// コンストラクタメソッド
		/// </summary>
		public MainFormStripMenu() {
			InitializeComponent();
		}// End Method


		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
		// Pubilc Method
		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
		// Private Method
		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
		// Control Method
		//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

		/// <summary>
		/// 「ファイル」→「アプリケーションの終了」の処理内容
		/// </summary>
		/// <param name="sender"></param>
		/// <param name="e"></param>
		private void StripMenuExit_Click(object sender, EventArgs e) {
			// この1行で済むらしい……?
			MenuExitClick?.Invoke(this, e);
		}// End Method
	}// End Class
}
